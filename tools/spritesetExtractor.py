def Parse(spritesetIndex):
    addr = file.tell()
    spriteset = []
    while True:
        spriteId = int.from_bytes(file.read(1), "little")
        graphicsRow = int.from_bytes(file.read(1), "little")
        spriteset.append((spriteId, graphicsRow))
        if spriteId == 0:
            break

    result = f"const u8 sSpriteset{spritesetIndex}[{len(spriteset)*2}] = " + "{\n"
    for (spriteId, graphicsRow) in spriteset:
        if spriteId == 0:
            result += "    SPRITESET_EMPTY\n"
        else:
            result += f"    {spriteNames[spriteId]}, {graphicsRow},\n"
    result += "};\n"
    #result = f"extern const u8 sSpriteset{spritesetIndex}[{len(spriteset)*2}];"
    return result

file = open("../mf_us_baserom.gba", "rb")
spritesetPointers = []
def Func():
    file.seek(0x79add8)
    for spritesetIndex in range(0x82):
        spritesetPointers.append((int.from_bytes(file.read(4), "little"), spritesetIndex))

    for (spritesetPointer, spritesetIndex) in sorted(spritesetPointers):
        file.seek(spritesetPointer & 0x1FFFFFF)
        print(Parse(spritesetIndex))

    print("const u8* const sSpritesetPointers[SPRITESET_END] = {")
    for spritesetIndex in range(0x82):
        print(f"    [SPRITESET_{spritesetIndex}] = sSpriteset{spritesetIndex},")
    print("};")

    print("enum SpritesetId {")
    for spritesetIndex in range(0x82):
        print(f"    SPRITESET_{spritesetIndex},")
    print("};")

    file.close()
